def joker_card_score_effects(jokers, card_scored, gamestate):
    """
    Calculate the effects of jokers that trigger when a card is scored.
    :param jokers: List of jokers that are in play.
    :param card_scored: The card that has been scored.
    :return: The modified score for the card.
    """
    effects = {"chips": 0, "mult": 0, "mult_mult": 1, "callbacks": [], "synergy": 0}
    smeared = any(joker.name == "Smeared Joker" for joker in jokers)
    for joker in jokers:
        if joker.name == "Greedy Joker":
            if (
                card_scored.suit == "Diamonds"
                or smeared
                and card_scored.suit == "Hearts"
            ):
                effects["mult"] += 3
                effects[
                    "synergy"
                ] += 0.5  # less synergy because it can trigger multiple times per hand
        elif joker.name == "Lusty Joker":
            if (
                card_scored.suit == "Hearts"
                or smeared
                and card_scored.suit == "Diamonds"
            ):
                effects["mult"] += 3
                effects["synergy"] += 0.5
        elif joker.name == "Wrathful Joker":
            if card_scored.suit == "Spades" or smeared and card_scored.suit == "Clubs":
                effects["mult"] += 3
                effects["synergy"] += 0.5
        elif joker.name == "Gluttonous Joker":
            if card_scored.suit == "Clubs" or smeared and card_scored.suit == "Spades":
                effects["mult"] += 3
                effects["synergy"] += 0.5
        elif joker.name == "Hiker":
            card_scored.hiker_chips += 5
        elif joker.name == "Golden Ticket":
            if card_scored.enhancement == BaseCard.Enhancements.GOLD:
                gamestate.dollars += 4
        elif joker.name == "Scary Face" and card_scored.is_face_card(jokers=jokers):
            effects["chips"] += 30
            effects["synergy"] += 0.8
        elif joker.name == "Even Steven" and card_scored.value in [2, 4, 6, 8, 10]:
            effects["mult"] += 4
            effects["synergy"] += 0.5
        elif joker.name == "Odd Todd" and card_scored.value in [3, 5, 7, 9, 14]:
            effects["chips"] += 31
            effects["synergy"] += 0.5
        elif joker.name == "Scholar" and card_scored.value == 14:
            effects["chips"] += 20
            effects["mult"] += 4
            effects["synergy"] += 1
        elif joker.name == "Walkie Talkie" and card_scored.value in [4, 10]:
            effects["chips"] += 10
            effects["mult"] += 4
            effects["synergy"] += 0.8
        elif joker.name == "Smiley Face" and card_scored.is_face_card(jokers=jokers):
            effects["mult"] += 5
            effects["synergy"] += 0.8
        elif joker.name == "Fibonacci" and card_scored.value in [2, 3, 5, 8, 14]:
            effects["mult"] += 8
            effects["synergy"] += 0.5
        elif joker.name == "Wee Joker":
            if card_scored.value == 2:
                joker.state["chips"] += 8
        elif joker.name == "Rough Gem":
            if card_scored.suit == "Diamonds":
                effects["callbacks"].append(("earn_money", 1))
                effects["synergy"] += 0.5
        elif joker.name == "Business Card":
            if card_scored.is_face_card(jokers=jokers):
                if random.random() < 0.5:
                    effects["callbacks"].append(("earn_money", 2))
                effects["synergy"] += 0.5
        elif joker.name == "Bloodstone":
            if card_scored.suit == "Hearts":
                if random.random() < 0.5:
                    effects["mult_mult"] *= 1.5
                effects["synergy"] += 0.5
        elif joker.name == "Arrowhead":
            if card_scored.suit == "Spades":
                effects["chips"] += 50
                effects["synergy"] += 0.5
        elif joker.name == "Onyx Agate":
            if card_scored.suit == "Clubs":
                effects["mult"] += 7
                effects["synergy"] += 0.5
        elif joker.name == "Photograph":
            if card_scored.is_face_card(jokers=jokers) and not joker.state.get(
                "triggered_this_hand", False
            ):
                joker.state["triggered_this_hand"] = True
                effects["mult_mult"] *= 2
        elif joker.name == "Triboulet":
            if card_scored.value in [12, 13]:
                effects["mult_mult"] *= 2
                effects["synergy"] += 0.5
        elif joker.name in ["Blueprint", "Brainstorm"]:
            # For right now we are going to randomly select targets to copy
            copyable_jokers = [j for j in jokers if j.copyable()]
            if len(copyable_jokers) > 0:
                copied_joker = random.choice(copyable_jokers)
                copied_joker = deepcopy(copied_joker)
                copied_effects = joker_card_score_effects(
                    [copied_joker], card_scored, gamestate
                )

                effects["chips"] += copied_effects["chips"]
                effects["mult"] += copied_effects["mult"]
                effects["mult_mult"] *= copied_effects["mult_mult"]
                effects["synergy"] += copied_effects["synergy"]
                effects["callbacks"].extend(
                    [x for x in copied_effects["callbacks"] if "destroy" not in x[0]]
                )
        elif joker.name == "Vampire":
            if card_scored.enhancement != BaseCard.Enhancements.NORMAL:
                joker.state["mult_mult"] += 0.1
                card_scored.enhancement = BaseCard.Enhancements.NORMAL
    return effects


def joker_triggered_effects(
    jokers,
    cards_played,
    cards_scored,
    cards_in_hand,
    hand_type,
    hands_left,
    discards_left,
    hand_stats,
    deck,
    dollars,
    gamestate,
):
    effects = {"chips": 0, "mult": 0, "mult_mult": 1, "callbacks": [], "synergy": 0}

    for joker in jokers:
        if joker.edition == BaseCard.Editions.HOLOGRAPHIC:
            effects["mult"] += 10
        elif joker.edition == BaseCard.Editions.POLYCHROME:
            effects["mult_mult"] *= 1.5
        elif joker.edition == BaseCard.Editions.FOIL:
            effects["chips"] += 50

        if joker.name == "Joker":
            effects["mult"] += 4
        elif joker.name == "Hologram":
            effects["mult_mult"] *= joker.state["mult_mult"]
        elif joker.name == "Driver's License":
            enh_count = 0
            for card in deck.all_cards:
                if card.enhancement != BaseCard.Enhancements.NORMAL:
                    enh_count += 1
            if enh_count >= 16:
                effects["mult_mult"] *= 3
        elif joker.name == "Fortune Teller":
            effects["mult"] += gamestate.tarot_used
        elif joker.name == "Canio":
            effects["mult_mult"] *= joker.state["mult_mult"]
        elif joker.name == "Yorick":
            effects["mult_mult"] *= joker.state["mult_mult"]
        elif joker.name == "Glass Joker":
            effects["mult_mult"] *= joker.state["mult_mult"]
        elif joker.name == "Supernova":
            play_count = hand_stats[hand_type].play_count
            effects["mult"] += play_count
            effects["synergy"] += play_count / 10.0
        elif joker.name == "Bull":
            effects["chips"] += 2 * dollars
        elif joker.name == "Bootstraps":
            effects["mult"] += 2 * (dollars // 5)
        elif joker.name == "Card Sharp":
            if hand_stats[hand_type].played_this_blind:
                effects["mult_mult"] *= 3
                effects["synergy"] += 1
        elif joker.name == "Blue Joker":
            effects["chips"] += 2 * len(deck.remaining_cards)
            effects["synergy"] += len(deck.remaining_cards) / 100.0
        elif joker.name == "Erosion":
            below_start = max(0, 52 - len(deck.all_cards))
            effects["mult"] += 4 * below_start
            effects["synergy"] += below_start / 10.0
        elif joker.name == "Square Joker":
            if len(cards_played) == 4:
                joker.state["chips"] += 4
                effects["synergy"] += 0.5
            effects["chips"] += joker.state["chips"]
        elif joker.name == "Steel Joker":
            effects["mult_mult"] *= 1 + 0.2 * len(
                [
                    card
                    for card in gamestate.deck.all_cards
                    if card.enhancement == BaseCard.Enhancements.STEEL
                ]
            )
        elif joker.name == "Stone Joker":
            effects["chips"] *= 25 * len(
                [
                    card
                    for card in gamestate.deck.all_cards
                    if card.enhancement == BaseCard.Enhancements.STONE
                ]
            )
        elif joker.name == "Stuntman":
            effects["chips"] += 250
        elif joker.name == "Joker Stencil":
            non_stencil_jokers = [j for j in jokers if j.name != "Joker Stencil"]
            effects["mult_mult"] *= gamestate.current_joker_limit - len(
                non_stencil_jokers
            )
        elif joker.name == "Jolly Joker":
            if hand_type in [
                "Pair",
                "Two Pair",
                "Three of a Kind",
                "Full House",
                "Four of a Kind",
            ]:
                effects["mult"] += 8
                effects["synergy"] += 1
        elif joker.name == "Zany Joker":
            if hand_type in ["Three of a Kind", "Full House", "Four of a Kind"]:
                effects["mult"] += 12
                effects["synergy"] += 1
        elif joker.name == "Mad Joker":
            if hand_type in ["Two Pair", "Full House"]:
                effects["mult"] += 10
                effects["synergy"] += 1
        elif joker.name == "Crazy Joker":
            if hand_type in ["Straight", "Straight Flush"]:
                effects["mult"] += 12
                effects["synergy"] += 1
        elif joker.name == "Droll Joker":
            if hand_type in ["Flush", "Straight Flush"]:
                effects["mult"] += 10
                effects["synergy"] += 1
        elif joker.name == "Sly Joker":
            if hand_type in [
                "Pair",
                "Two Pair",
                "Three of a Kind",
                "Full House",
                "Four of a Kind",
            ]:
                effects["chips"] += 50
                effects["synergy"] += 1
        elif joker.name == "The Duo":
            if hand_type in [
                "Pair",
                "Two Pair",
                "Three of a Kind",
                "Full House",
                "Four of a Kind",
            ]:
                effects["mult_mult"] *= 2
                effects["synergy"] += 1
        elif joker.name == "Wily Joker":
            if hand_type in ["Three of a Kind", "Full House", "Four of a Kind"]:
                effects["chips"] += 100
                effects["synergy"] += 1
        elif joker.name == "The Trio":
            if hand_type in ["Three of a Kind", "Full House", "Four of a Kind"]:
                effects["mult_mult"] *= 3
                effects["synergy"] += 1
        elif joker.name == "The Family":
            if hand_type in ["Four of a Kind"]:
                effects["mult_mult"] *= 4
                effects["synergy"] += 1
        elif joker.name == "Clever Joker":
            if hand_type == "Two Pair":
                effects["chips"] += 80
                effects["synergy"] += 1
        elif joker.name == "Devious Joker":
            if hand_type in ["Straight", "Straight Flush"]:
                effects["chips"] += 100
                effects["synergy"] += 1
        elif joker.name == "The Order":
            if hand_type in ["Straight", "Straight Flush"]:
                effects["mult_mult"] *= 3
                effects["synergy"] += 1
        elif joker.name == "Crafty Joker":
            if hand_type in ["Flush", "Straight Flush"]:
                effects["chips"] += 80
                effects["synergy"] += 1
        elif joker.name == "The Tribe":
            if hand_type in ["Flush", "Straight Flush"]:
                effects["mult_mult"] *= 2
                effects["synergy"] += 1
        elif joker.name == "Half Joker":
            if len(cards_played) <= 3:
                effects["mult"] += 20
                effects["synergy"] += 1
        elif joker.name == "Misprint":
            effects["mult"] += random.randint(0, 23)
        elif joker.name == "Raised Fist":
            effects["mult"] += min([card.value for card in cards_in_hand] + [0])
        elif joker.name == "Baron":
            effects["mult_mult"] *= 1.5 * sum(
                1 for card in cards_in_hand if card.value == 13
            )
            effects["synergy"] += sum(1 for card in cards_in_hand if card.value == 13)
        elif joker.name == "Runner":
            if hand_type in ["Straight", "Straight Flush"]:
                joker.state["chips"] += 15
                effects["synergy"] += 1
            effects["chips"] += joker.state["chips"]
        elif joker.name == "Shoot the Moon":
            match_count = sum(1 if card.value == 12 else 0 for card in cards_in_hand)
            effects["synergy"] += match_count
            effects["mult"] += match_count * 13
        elif joker.name == "Abstract Joker":
            effects["mult"] += 3 * len(jokers)
        elif joker.name == "Ice Cream":
            effects["chips"] += joker.state["chips"]
            joker.state["chips"] -= 5
            joker.state["chips"] = max(joker.state["chips"], 0)
